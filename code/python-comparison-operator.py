'''```python'''
# 数値の比較
a = 10
b = 5
print(a > b)  # 出力結果: True
print(a < b)  # 出力結果: False
print(a == b)  # 出力結果: False
print(a != b)  # 出力結果: True

# 文字列の比較
str1 = "apple"
str2 = "banana"
print(str1 > str2)  # 出力結果: False
print(str1 < str2)  # 出力結果: True
print(str1 == str2)  # 出力結果: False
print(str1 != str2)  # 出力結果: True
'''```'''


'''```python'''
# 複数の比較演算子の使用
a = 10
b = 5
c = 3
print(a > b and a > c)  # 出力結果: True
print(a > b or a < c)  # 出力結果: True
